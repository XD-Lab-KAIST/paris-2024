function getRandom(a: number, b: number) {
  return Math.random() * (b - a) + a;
}

function getRandomInt(a: number, b: number) {
  return Math.floor(getRandom(a, b));
}

function getRandomIntNormalise(a: number) {
  return getRandomInt(-a, a) / a;
}

export function textGenerator() {
  let texts = [];
  for (let i = 0; i < 200; i++) {
    let text = BAG_OF_WORDS[Math.floor(Math.random() * BAG_OF_WORDS.length)];
    let show = getRandom(getRandom(0.1, 0.3), 0.3);
    let fontSize = getRandom(0.5, 1);
    let position = [getRandomIntNormalise(10), getRandomIntNormalise(10), getRandomIntNormalise(10)];
    texts.push({ text, show, fontSize, position });
  }

  console.log(texts);
  return texts;
}

